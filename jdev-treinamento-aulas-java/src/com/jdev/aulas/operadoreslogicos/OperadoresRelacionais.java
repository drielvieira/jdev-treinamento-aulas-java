package com.jdev.aulas.operadoreslogicos;

public class OperadoresRelacionais {

	public static void main(String[] args) {
		int nota1 = 85;
		int nota2 = 90;
		
		
		System.out.println("NOTA1 == NOTA2 ? : " + "["+ (nota1 == nota2) + "]");
		System.out.println("NOTA1 >= NOTA2 ? : " + "["+ (nota1 >= nota2) + "]");
		System.out.println("NOTA1 <= NOTA2 ? : " + "["+ (nota1 <= nota2) + "]");
		System.out.println("NOTA1 != NOTA2 ? : " + "["+ (nota1 != nota2) + "]");
		System.out.println("NOTA1 > NOTA2 ? : " + "["+ (nota1 > nota2) + "]");
		System.out.println("NOTA1 < NOTA2 ? : " + "["+ (nota1 < nota2) + "]");

	}

}
