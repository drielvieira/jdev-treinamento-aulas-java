package com.jdev.aulas.tiposdedados;

public class TipoString {

	public static void main(String[] args) {
		/* String em java sempre usa para texto de qualquer tamanho
		 * String não é um tipo primitivo */
		
		String texto = "string é o tipo de dado que usamos para texto";
		System.out.println(texto);
		
		String texto2 = "Mais uma frase para o exemplo de string";
		System.out.println(texto2);

	}

}
