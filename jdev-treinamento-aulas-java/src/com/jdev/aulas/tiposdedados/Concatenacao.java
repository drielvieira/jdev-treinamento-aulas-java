package com.jdev.aulas.tiposdedados;

public class Concatenacao {

	public static void main(String[] args) {
		/* Conctenando variaveis e textos 
		 * Unir e juntas dados */
		
		String nome = "Adriel";
		String cpf = "000-000-00";
		String mesNacimento = "Setembro";
		String endereco = "Av. Paulista";
		
		System.out.println("Meu nome é " + nome);
		System.out.println("Meu CPF é " + cpf);
		System.out.println("Nasci no mês de " + mesNacimento);
		System.out.println("Meu endereço é " + endereco + " número 1485");

	}

}
